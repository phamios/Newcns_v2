                    <p>We could go on, but the bottom line is that, while much of this 
                        clutter can either be deleted or at least put away so it’s not 
                        noticeable, the out-of-the-box software experience of the Harman Kardon 
                        M8 is a cluttered, confusing, unattractive mess that’s desperately in 
                        need of some Hoarders-level digital housecleaning. To say that’s 
                        disappointing in a top-of-the-line, high-priced smartphone in 2014 is 
                        putting it nicely.</p>
                    <h3>Spint’s 4G is improving, but still far behind</h3>
                    <p>Sprint’s tri-bad 4G Spark network is definitely better than it was when we first saw it last year on the HTC One Max and <a data-event-param="Amazon.com" data-event-action="keyword-tool" data-event-type="affiliate-click" class="trackable" rel="nofollow" target="_blank" href="http://amzn.to/13f2gSi">Samsung Galaxy S4</a>
                        Mini—at least in the greater NYC area. While commuting and walking 
                        around Manhattan, Jersey City, and Hoboken, we were only occasionally 
                        bumped down to the slower 4G, usually while indoors.</p>
                    <p>But even on the speedier Spark network in Jersey City, the Harman 
                        Kardon M8 was only able to achieve 7.86Mbps up and 1.77Mbps down in 
                        Ookla’s Speedtest app. By comparison, our Samsung <a data-event-param="Amazon.com" data-event-action="keyword-tool" data-event-type="affiliate-click" class="trackable" rel="nofollow" target="_blank" href="http://amzn.to/13f2gSi">Galaxy S4</a>
                        on T-Mobile blew past it at 34.9Mbps up and 19.83 down. Clearly, Sprint
                        still has a lot of network improvements to accomplish if it wants to 
                        catch up.</p>
                    <h3>Battery life bump</h3>
                    <p>Battery life is a definite improvement over the original HTC one. We 
                        spent a full day installing apps, running benchmarks, taking photos and 
                        uploading a few to Facebook, sending texts, and otherwise heavily 
                        leaning on the battery. And only after 15-and-a-half hours of heavy use 
                        did the battery dip below 15 percent, kicking on the power-saving mode. 
                        Nearly everyone should be able to get through a full day with the M8. 
                        Light users may be able to get through two—especially if you make use of
                        the Battery Saver mode.</p>
                    <p><a href="http://www.digitaltrends.com/wp-content/uploads/2014/05/HTC-One-M8-Harman-Kardon-edition-bottom.jpg"><span class="dt-auto-zoom"><span></span></span><span class="image-holder"></span></a></p>
                    <p>Just be careful about how you place the phone in your pocket. We’re 
                        not sure if it’s because of the button placement, with the power button 
                        on the top (and the headphone jack on the bottom), or the phone’s 
                        ability to unlock the screen via gestures, without pressing the power 
                        button. But while walking a few miles with the phone in our back poccket
                        on a Sunday afternoon, we found the screen was already on more than 
                        once. Once, it was several minutes into recording a video of the inside 
                        of our pocket.</p>
                    <h3>Conclusion</h3>
                    <p>The HTC One Harman/Kardon Edition isn’t a bad phone. In fact, it’s 
                        far from that, thanks largely to the mostly great design and hardware 
                        that it borrows from the standard version of the One M8. And whether or 
                        not you put much serious stock in the audio magic that Harman Kardon 
                        delivers to the device (we don’t), the bundled headset is an excellent 
                        upgrade over what we’re used to finding in the box with new smartphones.</p>
                    <p><strong>The phone’s real problems are two-fold: </strong></p>
                    <ul>
                        <li><strong>Junk software:</strong> First, it’s loaded up with the kind 
                            of software gunk and junk we’d expect (though still dislike) on a budget
                            handset, not a high-priced flagship. The extra apps, backgrounds, and 
                            widgets manage to make one of the most attractive phones on the planet 
                            look awful. It’s fine if you’re willing to take the time to uninstall 
                            what you can and hide the rest. But why should you have to do that when 
                            you’re already buying one of the most expensive phones on the market?</li>
                        <li><strong>High price:</strong> And then there’s price. If you’re 
                            locked into Sprint, then the Harman Kardon M8 is fine if you also need a
                            new set of headphones. But taking out the confusing subsidies and 
                            payment plans to see how these phones really cost, we see the Harman 
                            Kardon M8 costs $680, while the standard HTC One M8 on Sprint sells for 
                            $650. So the Harman Kardon model is a relative deal if you like its 
                            looks and are also in the market for a decent set of earbuds.</li>
                    </ul>
                    <p>But there are of course, other networks to consider. All the major 
                        carriers sell the HTC One M8. But at the time of this writing, <a data-event-param="Verizon" data-event-action="keyword-tool" data-event-type="affiliate-click" class="trackable" rel="nofollow" target="_blank" href="http://www.anrdoezrs.net/click-2942700-11365093">Verizon</a>
                        was selling their M8 for $600, T-Mobile was offering the phone for $636
                        (24 monthly payments of $26.50). And AT&amp;T was selling the phone for
                        $640.</p>
                    <p>Considering the relatively high price of Sprint M8 offerings, 
                        combined with it’s still underperforming Spark 4G network, you’re 
                        probably better off looking elsewhere, unless you’re part of a Sprint 
                        Framily. If you are in it to win it on Sprint, a standard M8 is cheaper 
                        and easier on the eyes.</p>

                    <h3>Highs</h3>
                    <ul>
                        <li>Same great hardware as standard One M8</li>
                        <li>Excellent bundled headphones</li>
                        <li>Spotify perks (if you’re on a Framily plan)</li>
                    </ul>
                    <h3>Lows</h3>
                    <ul>
                        <li>Sprint tweaks uglify the UI</li>
                        <li>Still slippery in the hand</li>
                        <li>Competing flagships are waterproof</li>
                        <li>Audio tweaks have a limited appeal</li>
                        <li>Overloaded with pre-installed apps</li>
                    </ul>

